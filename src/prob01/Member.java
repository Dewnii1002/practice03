package prob01;

public class Member {
	
	private String id;
	private String name;
	private int point;
	
	public void setName (String name) {
		this.name = name;
	}
	
	public String getName (String name) {
		return name;
	}
	
	public void setPoint (int point) {
		this.point = point;
	}
	
	public int getPoint (int point) {
		return point;
	}
	
	public void setId (String id) {
		this.id = id;
	}
	
	public String getId (String id) {
		return id;
	}
	
	
}